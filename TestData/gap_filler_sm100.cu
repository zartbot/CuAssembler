// ================================================================
// UTMACPL / UTMASTG / UTMAPF / UTMRPL — TMA operations
// These need tensormap descriptors; compile-only verification.
// ================================================================
extern "C" __global__ void k_tma_ops(void* smem_ptr, const void* tmap_ptr,
                                      unsigned long long* mbar_ptr) {
    unsigned smem = (unsigned)(uintptr_t)smem_ptr;
    unsigned mbar = (unsigned)(uintptr_t)mbar_ptr;

    // UTMACPL — TMA tile load (1D)
    asm volatile(
        "cp.async.bulk.tensor.1d.shared::cta.global.tile"
        ".mbarrier::complete_tx::bytes [%0], [%1, {%2}], [%3];\n\t"
        :: "r"(smem), "l"(tmap_ptr), "r"(0), "r"(mbar) : "memory");

    // UTMACPL — TMA tile load (2D)
    asm volatile(
        "cp.async.bulk.tensor.2d.shared::cta.global.tile"
        ".mbarrier::complete_tx::bytes [%0], [%1, {%2, %3}], [%4];\n\t"
        :: "r"(smem), "l"(tmap_ptr), "r"(0), "r"(0), "r"(mbar) : "memory");

    // UTMASTG — TMA store (1D)
    asm volatile(
        "cp.async.bulk.tensor.1d.global.shared::cta.tile.bulk_group"
        " [%0, {%1}], [%2];\n\t"
        "cp.async.bulk.commit_group;\n\t"
        "cp.async.bulk.wait_group 0;\n\t"
        :: "l"(tmap_ptr), "r"(0), "r"(smem) : "memory");

    // UTMAPF — TMA prefetch (1D)
    asm volatile(
        "cp.async.bulk.prefetch.tensor.1d.L2.global.tile"
        " [%0, {%1}];\n\t"
        :: "l"(tmap_ptr), "r"(0) : "memory");

    // UTMRPL — tensormap replace
    asm volatile(
        "tensormap.replace.tile.global_address.shared::cta.b1024.b64"
        " [%0], %1;\n\t"
        :: "r"(smem), "l"((unsigned long long)0x1000) : "memory");

    asm volatile(
        "tensormap.replace.tile.rank.shared::cta.b1024.b32"
        " [%0], %1;\n\t"
        :: "r"(smem), "r"(2) : "memory");
}

