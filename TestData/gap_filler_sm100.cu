// ================================================================
// SETMAXNREG — dynamic register reallocation
// ================================================================
__global__ __launch_bounds__(128, 2)
void k_setmaxnreg_v1(float* out) {
    int warp_id = threadIdx.x / 32;
    if (warp_id < 2) {
        asm volatile("setmaxnreg.inc.sync.aligned.u32 40;");
        out[threadIdx.x] = (float)threadIdx.x;
    } else {
        asm volatile("setmaxnreg.dec.sync.aligned.u32 40;");
        out[threadIdx.x + 128] = (float)threadIdx.x;
    }
}

__global__ __launch_bounds__(256, 1)
void k_setmaxnreg_v2(float* out) {
    int warp_id = threadIdx.x / 32;
    if (warp_id < 4) {
        asm volatile("setmaxnreg.inc.sync.aligned.u32 48;");
        out[threadIdx.x] = (float)(threadIdx.x * 2);
    } else {
        asm volatile("setmaxnreg.dec.sync.aligned.u32 48;");
        out[threadIdx.x + 256] = (float)(threadIdx.x * 3);
    }
}

// VMAD — emulated via IMAD on sm_70+; skip inline asm
__device__ __noinline__ int vmad_ops(int a, int b, int c) {
    return a * b + c;
}

// ================================================================
// UREDUX — uniform redux
// ================================================================
__device__ __noinline__ unsigned uredux_ops(unsigned val) {
    unsigned r1, r2;
    r1 = __reduce_add_sync(0xffffffff, (int)val);
    r2 = __reduce_or_sync(0xffffffff, val);
    return r1 + r2;
}

// ================================================================
// Additional register diversity for existing keys
// ================================================================
__device__ __noinline__ float ffma_variety(float a, float b, float c, float d, float e, float f) {
    float r = fmaf(a, b, c);
    r = fmaf(d, e, f);
    r = fmaf(a, d, r);
    r = fmaf(b, e, r);
    r = fmaf(c, f, r);
    return r;
}

__device__ __noinline__ int iadd3_variety(int a, int b, int c, int d, int e, int f) {
    return (a + b + c) + (d + e + f);
}

__device__ __noinline__ void ldg_stg_variety(float* out, const float* in, int idx) {
    // 8-byte
    double v = *(double*)(in + idx);
    *(double*)(out + idx) = v;
    // 16-byte
    float4 v4 = *(float4*)(in + idx + 4);
    *(float4*)(out + idx + 4) = v4;
    // Single byte/short
    char c = *((char*)in + idx);
    *((char*)out + idx + 8) = c;
    short s = *((short*)in + idx);
    *((short*)out + idx + 10) = s;
}

__device__ __noinline__ void shf_variety(unsigned* out, unsigned a, unsigned b) {
    unsigned r;
    asm volatile("shf.l.wrap.b32 %0, %1, %2, 4;" : "=r"(r) : "r"(a), "r"(b));
    out[0] = r;
    asm volatile("shf.r.wrap.b32 %0, %1, %2, 8;" : "=r"(r) : "r"(a), "r"(b));
    out[1] = r;
    asm volatile("shf.l.clamp.b32 %0, %1, %2, 12;" : "=r"(r) : "r"(a), "r"(b));
    out[2] = r;
    asm volatile("shf.r.clamp.b32 %0, %1, %2, 16;" : "=r"(r) : "r"(a), "r"(b));
    out[3] = r;
    // 64-bit shifts
    unsigned hi, lo;
    asm volatile("shf.l.wrap.b32 %0, %1, %2, %3;" : "=r"(hi) : "r"(a), "r"(b), "r"(3u));
    asm volatile("shf.r.wrap.b32 %0, %1, %2, %3;" : "=r"(lo) : "r"(a), "r"(b), "r"(5u));
    out[4] = hi + lo;
}

// ================================================================
// Main kernel — call all gap-filling functions
// ================================================================
extern "C" __global__ void gap_filler_main(
    float* __restrict__ fout, int* __restrict__ iout,
    double* __restrict__ dout, const float* __restrict__ fin,
    int n)
{
    __shared__ alignas(8) unsigned long long smem_mbar[2];
    int t = threadIdx.x + blockIdx.x * blockDim.x;

    // BFE / BFI
    unsigned bfe_r = bfe_ops((unsigned)t, (unsigned)(t+1), (unsigned)(t+2));
    unsigned bfi_r = bfi_ops((unsigned)t, (unsigned)(t+1), (unsigned)(t+2));

    // DMNMX
    double dm = dmnmx_ops((double)t, (double)(t + 1));

    // FMNMX3 / IMNMX3
    float fm3 = fmnmx3_ops((float)t, (float)(t+1), (float)(t+2));
    int im3 = imnmx3_ops(t, t+1, t+2);

    // JMP (indexed branch)
    int jmp_r = jmp_ops(t);

    // LDU
    float ldu_r = ldu_ops(fin, t & 255);

    // MBAR
    if (threadIdx.x == 0) {
        mbar_ops(smem_mbar);
    }
    __syncthreads();

    // VMAD
    int vmad_r = vmad_ops(t, t+1, t+2);

    // UREDUX
    unsigned uredux_r = uredux_ops((unsigned)t);

    // Register diversity
    float fv = ffma_variety((float)t, (float)(t+1), (float)(t+2),
                            (float)(t+3), (float)(t+4), (float)(t+5));
    int iv = iadd3_variety(t, t+1, t+2, t+3, t+4, t+5);

    // LDG/STG diversity
    ldg_stg_variety(fout + 512, fin + 512, t & 63);

    // SHF diversity
    unsigned shf_out[5];
    shf_variety(shf_out, (unsigned)t, (unsigned)(t+1));

    // Store
    fout[t] = fv + fm3 + ldu_r + (float)bfe_r + (float)bfi_r;
    iout[t] = iv + im3 + jmp_r + vmad_r + (int)uredux_r + (int)shf_out[0];
    dout[t] = dm;
}
