// ================================================================
// tcgen05 sync operations: UTCFENCE, UTCCOMMIT, UTCWAIT, UTCCP
// Using the same pattern as tcgen05_encoding_soup.cu that compiles cleanly.
// ================================================================
extern "C" __global__ void k_tcgen05_sync(uint64_t *out, uint64_t desc_a, uint64_t desc_b,
                                           uint64_t idesc_val, uint64_t nCols) {
    __shared__ alignas(8) unsigned long long mbar;
    asm volatile(
        "{\n\t"
        ".reg .pred p0;\n\t"
        ".reg .b32 tmem_addr, r0;\n\t"

        // UTCFENCE — fence before thread sync
        "tcgen05.fence::before_thread_sync;\n\t"

        // MMA — reuse alloc pattern from known-good soup
        "tcgen05.alloc.cta_group::1.sync.aligned.shared::cta.b32 [%5], %4;\n\t"
        "tcgen05.relinquish_alloc_permit.cta_group::1.sync.aligned;\n\t"
        "ld.shared.b32 tmem_addr, [%5];\n\t"

        "tcgen05.mma.cta_group::1.kind::f16 [tmem_addr], %1, %2, %3, p0;\n\t"

        // UTCCOMMIT — commit to mbarrier
        "tcgen05.commit.cta_group::1.mbarrier::arrive::one.shared::cluster.b64 [%6];\n\t"

        // UTCFENCE — fence after thread sync
        "tcgen05.fence::after_thread_sync;\n\t"

        // UTCWAIT — wait for ld
        "tcgen05.wait::ld.sync.aligned;\n\t"

        // tcgen05.ld
        "tcgen05.ld.sync.aligned.32x32b.x1.b32 {r0}, [tmem_addr];\n\t"

        // UTCWAIT — wait for st
        "tcgen05.wait::st.sync.aligned;\n\t"

        // tcgen05.st
        "tcgen05.st.sync.aligned.32x32b.x1.b32 [tmem_addr], {r0};\n\t"

        // dealloc
        "tcgen05.dealloc.cta_group::1.sync.aligned.b32 tmem_addr, %4;\n\t"

        "st.global.b32 [%0], r0;\n\t"
        "}\n\t"
        :
        : "l"(out), "l"(desc_a), "l"(desc_b), "r"((uint32_t)idesc_val),
          "r"((uint32_t)nCols),
          "r"((unsigned)__cvta_generic_to_shared((void*)out)),
          "r"((unsigned)__cvta_generic_to_shared(&mbar))
        : "memory"
    );
}

