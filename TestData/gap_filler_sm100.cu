// gap_filler_sm100.cu — fill encoding gaps for SM_100/SM_103 repos.
// Targets opcodes missing from the initial repos build.
// Build: nvcc -O0 -std=c++17 -lineinfo -gencode arch=compute_103a,code=sm_103a -cubin gap_filler_sm100.cu
#include <cuda_fp16.h>
#include <cuda_bf16.h>
#include <cstdint>

// ================================================================
// BFE — bitfield extract
// ================================================================
__device__ __noinline__ unsigned bfe_ops(unsigned a, unsigned b, unsigned c) {
    unsigned r1, r2, r3, r4;
    asm volatile("bfe.u32 %0, %1, 0, 8;" : "=r"(r1) : "r"(a));
    asm volatile("bfe.u32 %0, %1, 4, 12;" : "=r"(r2) : "r"(b));
    asm volatile("bfe.s32 %0, %1, 8, 16;" : "=r"(r3) : "r"(c));
    asm volatile("bfe.u32 %0, %1, %2, %3;" : "=r"(r4) : "r"(a), "r"(b), "r"(c));
    return r1 + r2 + r3 + r4;
}

// ================================================================
// BFI — bitfield insert
// ================================================================
__device__ __noinline__ unsigned bfi_ops(unsigned a, unsigned b, unsigned c) {
    unsigned r1, r2, r3;
    asm volatile("bfi.b32 %0, %1, %2, 0, 8;" : "=r"(r1) : "r"(a), "r"(b));
    asm volatile("bfi.b32 %0, %1, %2, 4, 12;" : "=r"(r2) : "r"(b), "r"(c));
    asm volatile("bfi.b32 %0, %1, %2, %3, %4;" : "=r"(r3) : "r"(a), "r"(b), "r"(c), "r"(8u));
    return r1 + r2 + r3;
}

// ================================================================
// DMNMX — FP64 min/max
// ================================================================
__device__ __noinline__ double dmnmx_ops(double a, double b) {
    return fmin(a, b) + fmax(a, b);
}

// ================================================================
// FMNMX3, IMNMX3 — 3-input min/max (SM_100 new)
// Compiler may or may not emit these; force via nested calls at O2
// ================================================================
__device__ __noinline__ float fmnmx3_ops(float a, float b, float c) {
    float mn = fminf(fminf(a, b), c);
    float mx = fmaxf(fmaxf(a, b), c);
    return mn + mx;
}

__device__ __noinline__ int imnmx3_ops(int a, int b, int c) {
    int mn = min(min(a, b), c);
    int mx = max(max(a, b), c);
    return mn + mx;
}

// ================================================================
// JMP — indexed branch (via switch with many cases)
// ================================================================
__device__ __noinline__ int jmp_ops(int x) {
    switch (x & 0xf) {
        case 0: return x * 2;
        case 1: return x + 100;
        case 2: return x - 50;
        case 3: return x * 3;
        case 4: return x + 200;
        case 5: return x - 100;
        case 6: return x * 4;
        case 7: return x + 300;
        case 8: return x - 150;
        case 9: return x * 5;
        case 10: return x + 400;
        case 11: return x - 200;
        case 12: return x * 6;
        case 13: return x + 500;
        case 14: return x - 250;
        default: return x;
    }
}

// ================================================================
// LDU — load uniform (read-only texture/const path)
// ================================================================
__device__ __noinline__ float ldu_ops(const float* __restrict__ p, int idx) {
    float r;
    asm volatile("ldu.global.f32 %0, [%1];" : "=f"(r) : "l"(p + idx));
    return r;
}

// ================================================================
// MBAR — mbarrier operations
// ================================================================
__device__ __noinline__ void mbar_ops(unsigned long long* smem_mbar) {
    unsigned mbar_addr = (unsigned)(uintptr_t)smem_mbar;
    asm volatile(
        "mbarrier.init.shared::cta.b64 [%0], 1;\n\t"
        :: "r"(mbar_addr) : "memory");
    asm volatile(
        "mbarrier.arrive.shared::cta.b64 _, [%0];\n\t"
        :: "r"(mbar_addr) : "memory");
    asm volatile(
        "mbarrier.arrive.expect_tx.shared::cta.b64 _, [%0], 128;\n\t"
        :: "r"(mbar_addr) : "memory");
    asm volatile(
        "{\n\t"
        ".reg .pred p;\n\t"
        "mbarrier.test_wait.parity.shared::cta.b64 p, [%0], 0;\n\t"
        "}\n\t"
        :: "r"(mbar_addr) : "memory");
    // mbarrier.invalidate requires sm_90a; skip
}

